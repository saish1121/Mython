# 4.Write a program which display 5 times Marvellous on screen. 
# Output : 
# Marvellous 
# Marvellous 
# Marvellous 
# Marvellous 
# Marvellous

def Display():
    no = 1
    while no <= 5 :
        print("Marvellous")
        no = no + 1

def main():
    Display()

if __name__ == "__main__":
    main()